package classAssign;

public class Q7 {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        System.out.println("Value of a before swapping: "+a);
        System.out.println("Value of b before swapping: "+b);
        int c = a;
        a = b;
        b = c;
        System.out.println("Value of a before swapping: "+a);
        System.out.println("Value of b before swapping: "+b);

    }
}
